int main(void) {

    init_graphics();
    init_audio();

    game_t game;
    game_init(&game);

    while (!exit_requested()) {
        input_t input = get_input();
        game_update(&game, input);
        render_game(&game);
    }

    cleanup_graphics();
    return 0;

}

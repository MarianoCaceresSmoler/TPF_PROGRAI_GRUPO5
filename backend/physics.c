int check_entities_collision(entity_t entityA, entity_t entityB)
{
    return (
        entityA.x <= entityB.x + entityB.size &&
        entityA.x + entityA.size >= entityB.x &&
        entityA.y <= entityB.y + entityB.size &&
        entityA.y + entityA.size >= entityB.y
    );
}

